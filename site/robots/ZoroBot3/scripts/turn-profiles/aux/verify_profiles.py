# ????????? Helper: compute .start / .end manually ??????????????????????????????????????????????????????????????????????????????????????????????????????

def compute_start_end(profile, entry, exit):
    """Replicate SlalomTurnProfile.describe() logic."""
    xy = profile[['x', 'y']]
    before = norm(xy.iloc[0].values - entry.reference)
    before *= sign(norm(xy.iloc[1].values - entry.reference) - before)
    after = norm(xy.iloc[-1].values - exit.reference)
    after *= sign(norm(xy.iloc[-2].values - exit.reference) - after)
    return float(before), float(after)


# ????????? Analyze Entry/Exit Lines ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

def analyze_entry_exit(name, entry_tuple, exit_tuple):
    """Analyze the geometric relationship of entry/exit lines."""
    cell = classic.cell
    entry_line = Line(x=entry_tuple[0]*cell, y=entry_tuple[1]*cell, angle=entry_tuple[2])
    exit_line = Line(x=exit_tuple[0]*cell, y=exit_tuple[1]*cell, angle=exit_tuple[2])

    print(f"\n{'='*70}")
    print(f"  {name}")
    print(f"{'='*70}")
    print(f"  Entry line: ref=({entry_line.reference[0]:.4f}, {entry_line.reference[1]:.4f}), "
          f"angle={entry_tuple[2]:.4f} rad ({np.degrees(entry_tuple[2]):.1f}??), "
          f"slope={entry_line.slope:.4f}, intercept={entry_line.intercept:.4f}")
    print(f"  Exit line:  ref=({exit_line.reference[0]:.4f}, {exit_line.reference[1]:.4f}), "
          f"angle={exit_tuple[2]:.4f} rad ({np.degrees(exit_tuple[2]):.1f}??), "
          f"slope={exit_line.slope:.4f}, intercept={exit_line.intercept:.4f}")

    # Check if lines are parallel
    try:
        intersection = lines_intersection(entry_line, exit_line)
        print(f"  Intersection: ({intersection[0]:.6f}, {intersection[1]:.6f})")
        return entry_line, exit_line, intersection
    except ValueError as e:
        print(f"  LINES ARE PARALLEL! ({e})")
        return entry_line, exit_line, None


